public class ForNewClass
 {
    public static void main(String args[])
    {
        int n=4;
        for(int i=0;i<n;i++)
        {
            for(int j=1;j<=i;j++)
            {
                System.out.print(" ");
            }
            for(int j1=0;j1<n-i;j1++)
            {
                System.out.print("*");
            }
            System.out.println();
        }
    }
    
}
